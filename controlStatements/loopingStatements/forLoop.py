# sample loop to print the values 1 to 10

for i in range(11):
    print(i)
print("loop is completed")


# to print even numbers only
for i in range(0,11,2):
    print(i)

# to print reverse order
for i in range(10,0,-1):
    print(i)

# to print reverse order negative values
for i in range(-10,0,1):
    print(i)