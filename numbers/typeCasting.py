# into to float typeCast

intt=20
print("int to float")
print("before typecast")
print(type(intt))

print("after typecast")
print(type(float(intt)))
print(float(intt))

# float to int typecast

flot=20.2
print("float to int")
print("before typecast")
print(type(flot))

print("after typecast")
print(type(int(flot)))
print(int(flot))